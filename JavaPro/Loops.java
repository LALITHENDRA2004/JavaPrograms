public class Loops {
    public static void main(String[] args) {
        for(int i = 1 ; i <= 10 ; i++) { // for(init ; cond ; incre/decre)
            System.out.println(i * i);
        }

        int j = 1; // initialization
        while(j <= 10) { // condition
            System.out.println(j * j);
            j++; //updation
        }
    }
}
